mood=input ("How is your mood? ")

if mood == "happy":
    print("It is great to see you happy!")
elif mood=="nervous":
    print("Take a deep breath 3 times.")
elif mood=="sad":
    print("Cheer up, mate!")
elif mood=="excited":
    print("Glad things are working out for you!")
elif mood=="relaxed":
    print("Enjoy a pina colada!")
else:
    print("I don't recognize this mood")